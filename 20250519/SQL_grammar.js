/* Inner Join과 Outer Join의 차이점 */
// Inner Join은 두 테이블 간의 조건에 일치하는 행만 조회하는 방식으로, 교집합에 해당하는 데이터를 반환한다.
// 반면 Outer Join은 한 쪽 테이블에만 존재하는 데이터도 포함시켜 결과를 반환하는데 이 때 매칭되지 않은 값은 null로 표시된다.
// Outer Join에는 세 가지 종류가 있다.
// Left Outer Join은 왼쪽 테이블의 모든 데이터를 기준으로 조인하고,
// Right Outer Join은 오른쪽 테이블의 모든 데이터를 기준으로 조인하고,
// Full Outer Join은 양쪽 테이블 모두 포함한다.

/* Group By의 역할  */
// Group By는 특정 컬럼을 기준으로 데이터를 그룹화해서, 각 그룹마다 집계 함수를 적용할 수 있게 해준다.
// 여기서 집계 함수는 COUNT, SUM, AVG, MAX, MIN등이 포함된다.
// 이를 통해 데이터의 통계나 요약 정보를 쉽게 얻을 수 있다.

/* Group By와 Having */
// Group By는 데이터를 그룹으로 묶어주는 역할을 하고, Having은 이렇게 그룹화된 결과에 조건을 추가할 때 사용한다.
// 중요한 차이점은 WHERE은 그룹화 전에 조건을 적용하고, HAVING은 그룹화 후 집계된 데이터에 조건을 적용한다는 점이다.

/* Join에서 On과 Where의 차이 */
// ON은 조인 시 두 테이블 간 연결 조건을 지정하는 구문으로, 조인이 수행되기 전에 필터링 역할을 한다.
// 반면 WHERE은 조인이 완료된 후 결과에 대해 조건을 필터링하는 역할을 한다.
// 특히 Outer Join에서 ON과 WHERE의 위치에 따라 결과가 달라질 수 있어 주의해야한다.
